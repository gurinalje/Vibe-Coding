"""
Critic Agent for AI Agent Benchmark system.

This agent is responsible for evaluating the **quality of review opinions**
generated by the Reviewer Agent.  Instead of critiquing code directly, it
acts as a meta-reviewer that:

- Assesses whether each reported review issue is a true positive or a false
  positive (误报).
- Identifies issues that were missed (漏报 / false negatives).
- Maintains a set of review standards (审查标准) to calibrate expectations.
- Provides actionable improvement suggestions for the review process.
- Generates structured evaluation reports.
"""

import asyncio
import re
from typing import Any, Dict, List, Optional, Tuple
import logging
from dataclasses import dataclass, field
from datetime import datetime
from enum import Enum

from .base_agent import BaseAgent, AgentMessage

logger = logging.getLogger(__name__)


# ------------------------------------------------------------------ #
# Enums & data classes
# ------------------------------------------------------------------ #

class CriticismType(str, Enum):
    """Categories of evaluation feedback."""
    FALSE_POSITIVE = "false_positive"
    FALSE_NEGATIVE = "false_negative"
    SEVERITY_MISMATCH = "severity_mismatch"
    WRONG_CATEGORY = "wrong_category"
    MISSING_SUGGESTION = "missing_suggestion"
    DUPLICATE_ISSUE = "duplicate_issue"
    ACCURATE = "accurate"


class ReviewStandard(str, Enum):
    """Built-in review standards that the critic maintains."""
    NAMING_CONVENTION = "naming_convention"
    CODE_COMPLEXITY = "code_complexity"
    DOCUMENTATION = "documentation"
    SECURITY = "security"
    PERFORMANCE = "performance"
    BEST_PRACTICE = "best_practice"
    STYLE = "style"
    ERROR_HANDLING = "error_handling"
    DESIGN_PATTERN = "design_pattern"


@dataclass
class EvaluationItem:
    """Evaluation of a single review issue."""
    issue_id: str
    evaluation: CriticismType
    confidence: float  # 0.0 - 1.0
    reasoning: str
    suggested_severity: Optional[str] = None
    suggested_category: Optional[str] = None
    improvement_suggestion: Optional[str] = None

    def to_dict(self) -> Dict[str, Any]:
        """Convert evaluation item to dictionary."""
        result = {
            "issue_id": self.issue_id,
            "evaluation": self.evaluation.value,
            "confidence": self.confidence,
            "reasoning": self.reasoning,
        }
        if self.suggested_severity is not None:
            result["suggested_severity"] = self.suggested_severity
        if self.suggested_category is not None:
            result["suggested_category"] = self.suggested_category
        if self.improvement_suggestion is not None:
            result["improvement_suggestion"] = self.improvement_suggestion
        return result


@dataclass
class MissedIssue:
    """An issue that was missed by the original review."""
    id: str
    standard: ReviewStandard
    expected_category: str
    description: str
    severity: str
    example: Optional[str] = None
    line_hint: Optional[int] = None

    def to_dict(self) -> Dict[str, Any]:
        """Convert missed issue to dictionary."""
        return {
            "id": self.id,
            "standard": self.standard.value,
            "expected_category": self.expected_category,
            "description": self.description,
            "severity": self.severity,
            "example": self.example,
            "line_hint": self.line_hint,
        }


@dataclass
class CriticismResult:
    """Complete evaluation result for a review."""
    overall_assessment: str
    score: float  # 0-100, higher = review quality is better
    evaluations: List[EvaluationItem]
    missed_issues: List[MissedIssue]
    strengths: List[str]
    weaknesses: List[str]
    recommendations: List[str]
    metrics: Dict[str, Any]

    def to_dict(self) -> Dict[str, Any]:
        """Convert result to dictionary."""
        return {
            "overall_assessment": self.overall_assessment,
            "score": self.score,
            "evaluations": [e.to_dict() for e in self.evaluations],
            "missed_issues": [m.to_dict() for m in self.missed_issues],
            "strengths": self.strengths,
            "weaknesses": self.weaknesses,
            "recommendations": self.recommendations,
            "metrics": self.metrics,
        }


