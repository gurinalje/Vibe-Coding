    def _generate_overall_assessment(
        self,
        evaluations: List[EvaluationItem],
        missed_issues: List[MissedIssue],
        score: float,
    ) -> str:
        """Generate a human-readable overall assessment.

        Args:
            evaluations: List of evaluation items.
            missed_issues: List of missed issues.
            score: Calculated quality score.

        Returns:
            Overall assessment string.
        """
        false_positives = sum(1 for e in evaluations if e.evaluation == CriticismType.FALSE_POSITIVE)
        accurate = sum(1 for e in evaluations if e.evaluation == CriticismType.ACCURATE)

        parts = []
        parts.append(f"审查质量评分: {score:.1f}/100")
        parts.append(f"已评估 {len(evaluations)} 条审查意见 (其中 {accurate} 条准确)")
        parts.append(f"发现 {false_positives} 条误报")
        parts.append(f"发现 {len(missed_issues)} 条漏报")

        if score >= 85:
            parts.append("总体评价: 审查质量优秀")
        elif score >= 70:
            parts.append("总体评价: 审查质量良好，有少量改进空间")
        elif score >= 50:
            parts.append("总体评价: 审查质量一般，需要改进")
        else:
            parts.append("总体评价: 审查质量较差，需要大幅改进")

        return "。".join(parts) + "。"

    def _identify_strengths(
        self,
        evaluations: List[EvaluationItem],
        missed_issues: List[MissedIssue],
    ) -> List[str]:
        """Identify strengths in the review process.

        Args:
            evaluations: List of evaluation items.
            missed_issues: List of missed issues.

        Returns:
            List of strength descriptions.
        """
        strengths: List[str] = []

        accurate_count = sum(1 for e in evaluations if e.evaluation == CriticismType.ACCURATE)
        total = len(evaluations)

        if total > 0 and accurate_count / total > 0.8:
            strengths.append("审查意见准确率高 (>80%)")

        if not missed_issues:
            strengths.append("未遗漏重要的代码问题")

        if not any(e.evaluation == CriticismType.DUPLICATE_ISSUE for e in evaluations):
            strengths.append("审查意见无重复")

        severity_matches = sum(1 for e in evaluations if e.evaluation == CriticismType.ACCURATE)
        if total > 0 and severity_matches / total > 0.7:
            strengths.append("严重程度评估总体合理")

        if not strengths:
            strengths.append("审查覆盖了基本的代码规范检查")

        return strengths

    def _identify_weaknesses(
        self,
        evaluations: List[EvaluationItem],
        missed_issues: List[MissedIssue],
    ) -> List[str]:
        """Identify weaknesses in the review process.

        Args:
            evaluations: List of evaluation items.
            missed_issues: List of missed issues.

        Returns:
            List of weakness descriptions.
        """
        weaknesses: List[str] = []

        false_positives = sum(1 for e in evaluations if e.evaluation == CriticismType.FALSE_POSITIVE)
        if false_positives > 0:
            weaknesses.append(f"存在 {false_positives} 条误报，降低了审查可信度")

        severity_mismatches = sum(1 for e in evaluations if e.evaluation == CriticismType.SEVERITY_MISMATCH)
        if severity_mismatches > 0:
            weaknesses.append(f"存在 {severity_mismatches} 条严重程度不匹配的审查意见")

        duplicates = sum(1 for e in evaluations if e.evaluation == CriticismType.DUPLICATE_ISSUE)
        if duplicates > 0:
            weaknesses.append(f"存在 {duplicates} 条重复的审查意见")

        if missed_issues:
            weaknesses.append(f"遗漏了 {len(missed_issues)} 个代码问题")

        # Check missed issue categories
        missed_categories = set(m.standard.value for m in missed_issues)
        if missed_categories:
            weaknesses.append(f"漏报涉及的领域: {', '.join(missed_categories)}")

        if not weaknesses:
            weaknesses.append("暂未发现明显不足")

        return weaknesses

    def _generate_recommendations(
        self,
        evaluations: List[EvaluationItem],
        missed_issues: List[MissedIssue],
        language: str,
    ) -> List[str]:
        """Generate recommendations for improving the review process.

        Args:
            evaluations: List of evaluation items.
            missed_issues: List of missed issues.
            language: Programming language identifier.

        Returns:
            List of recommendation strings.
        """
        recommendations: List[str] = []

        false_positives = sum(1 for e in evaluations if e.evaluation == CriticismType.FALSE_POSITIVE)
        if false_positives > 0:
            recommendations.append(
                "减少误报: 增强代码匹配逻辑的精确度，在报告问题前验证代码片段确实存在"
            )

        severity_mismatches = sum(1 for e in evaluations if e.evaluation == CriticismType.SEVERITY_MISMATCH)
        if severity_mismatches > 0:
            recommendations.append(
                "校准严重程度: 根据问题类别调整严重程度评估，"
                "命名和风格问题通常为 info，复杂度和最佳实践问题通常为 warning"
            )

        # Recommendations based on missed issue categories
        missed_standards = set(m.standard.value for m in missed_issues)
        if ReviewStandard.SECURITY.value in missed_standards:
            recommendations.append("加强安全检查: 增加安全相关模式的检测覆盖")

        if ReviewStandard.DOCUMENTATION.value in missed_standards:
            recommendations.append("完善文档检查: 增加文档完整性的验证规则")

        if ReviewStandard.PERFORMANCE.value in missed_standards:
            recommendations.append("增强性能分析: 添加更多性能反模式的检测")

        if ReviewStandard.CODE_COMPLEXITY.value in missed_standards:
            recommendations.append("改进复杂度分析: 深化函数长度和嵌套深度的检查")

        # General recommendations
        if not recommendations:
            recommendations.append("审查质量良好，可继续优化以进一步提升准确性")

        return recommendations

    # ------------------------------------------------------------------ #
    # Metrics
    # ------------------------------------------------------------------ #

    def _calculate_metrics(
        self,
        evaluations: List[EvaluationItem],
        missed_issues: List[MissedIssue],
        review_result: Dict[str, Any],
    ) -> Dict[str, Any]:
        """Calculate detailed evaluation metrics.

        Args:
            evaluations: List of evaluation items.
            missed_issues: List of missed issues.
            review_result: Original review result from the Reviewer.

        Returns:
            Dictionary of evaluation metrics.
        """
        total_reported = len(review_result.get("issues", []))
        total_evaluated = len(evaluations)

        # Count by evaluation type
        type_counts: Dict[str, int] = {}
        for eval_item in evaluations:
            eval_type = eval_item.evaluation.value
            type_counts[eval_type] = type_counts.get(eval_type, 0) + 1

        accurate = type_counts.get("accurate", 0)
        false_positives = type_counts.get("false_positive", 0)
        severity_mismatches = type_counts.get("severity_mismatch", 0)
        duplicates = type_counts.get("duplicate_issue", 0)

        # Missed issue categories
        missed_categories: Dict[str, int] = {}
        for missed in missed_issues:
            cat = missed.expected_category
            missed_categories[cat] = missed_categories.get(cat, 0) + 1

        # Precision and recall estimates
        precision = accurate / max(1, total_reported) if total_reported > 0 else 1.0
        false_negative_count = len(missed_issues)
        recall = accurate / max(1, accurate + false_negative_count) if (accurate + false_negative_count) > 0 else 1.0

        return {
            "total_reported_issues": total_reported,
            "total_evaluated": total_evaluated,
            "accurate_count": accurate,
            "false_positive_count": false_positives,
            "severity_mismatch_count": severity_mismatches,
            "duplicate_count": duplicates,
            "missed_issue_count": len(missed_issues),
            "missed_issue_categories": missed_categories,
            "estimated_precision": round(precision, 3),
            "estimated_recall": round(recall, 3),
            "evaluations_by_type": type_counts,
        }
