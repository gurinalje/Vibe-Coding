# Critic Agent
# ------------------------------------------------------------------ #

class CriticAgent(BaseAgent):
    """Agent responsible for evaluating the quality of review opinions.

    Core capabilities:
    - Evaluate whether each review issue is accurate, a false positive,
      or a false negative.
    - Identify missed issues by scanning the source code against review
      standards.
    - Provide improvement suggestions for the review process.
    - Maintain and evolve review standards.
    - Generate structured evaluation reports.

    Usage::

        critic = CriticAgent(name="critic_v2")
        await critic.initialize()

        result = await critic.run_task({
            "code": "...",
            "language": "python",
            "review_result": { ... },   # output from ReviewerAgent
        })
    """

    def __init__(
        self,
        name: str = "critic",
        message_queue=None,
        config: Optional[Dict[str, Any]] = None,
    ):
        """Initialize critic agent.

        Args:
            name: Agent name identifier.
            message_queue: Shared message queue for inter-agent communication.
            config: Optional configuration overrides.
        """
        super().__init__(name=name, agent_type="critic", message_queue=message_queue, config=config)

        # Review standards and thresholds
        self.review_standards = self._load_review_standards()

        # Statistics
        self.evaluations_completed = 0
        self.false_positives_detected = 0
        self.false_negatives_detected = 0

